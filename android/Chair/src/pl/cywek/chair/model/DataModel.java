package pl.cywek.chair.model;

public class DataModel {

	/**
	 * PF – pochylenie przedniej części fotela góra/dół.
	 */
	private int mPF;

	/**
	 * PO – pochylenie oparcia fotela.
	 */
	private int mPO;
	
	/**
	 * SP – przesunięcie całego fotela przód/tył,
	 */
	private int mSP;

	/**
	 * UG – pochylenie tylnej części siedziska. 
	 */
	private int mUG;
	
	/**
	 * ZG – wysunięcie zagłówka góra/dół. 
	 */
	private int mZG;
	
	/**
	 * WF – wydłużenie siedziska fotela przód/tył.
	 */
	private int mWF;
	
	/**
	 * GG – przesunięcie wybrzuszenia oparcia pod kręgosłupem w położeniu góra/dół.
	 */
	private int mGG;
	
	/**
	 * GW – wybrzuszenie oparcia pod kręgosłupem.
	 */
	private int mGW;
	
	/**
	 * PZ – pochylenie zagłówka.
	 */
	private int mPZ;
	
	
		


	public int getmPF() {
		return mPF;
	}

	public void setmPF(int mPF) {
		this.mPF = mPF;
	}

	public int getmPO() {
		return mPO;
	}

	public void setmPO(int mPO) {
		this.mPO = mPO;
	}

	public int getmSP() {
		return mSP;
	}

	public void setmSP(int mSP) {
		this.mSP = mSP;
	}

	public int getmUG() {
		return mUG;
	}

	public void setmUG(int mUG) {
		this.mUG = mUG;
	}

	public int getmZG() {
		return mZG;
	}

	public void setmZG(int mZG) {
		this.mZG = mZG;
	}

	public int getmWF() {
		return mWF;
	}

	public void setmWF(int mWF) {
		this.mWF = mWF;
	}

	public int getmGG() {
		return mGG;
	}

	public void setmGG(int mGG) {
		this.mGG = mGG;
	}

	public int getmGW() {
		return mGW;
	}

	public void setmGW(int mGW) {
		this.mGW = mGW;
	}

	public int getmPZ() {
		return mPZ;
	}

	public void setmPZ(int mPZ) {
		this.mPZ = mPZ;
	}
	
	
}
